def add(a, b):
    return a + b

print(add(3, 4))
# Output:
# 7



def square(x):
    return x * x

print(square(5))
# Output:
# 25



def greet(name):
    return "Hello " + name

print(greet("Malika"))
# Output:
# Hello Malika



def is_even(n):
    return n % 2 == 0

print(is_even(6))
# Output:
# True



